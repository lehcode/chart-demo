  /**
   * Generate data
   *
   * @param {Number} m - Amount of results
   * @param {Number} startTs - Starting timestamp
   * @param {Number} step
   * @param {Number} endTs
   * @return {Array<Array<number>>}
   */
  protected _generateValues(
    m = 1,
    startTs = 1596128655,
    step = 10,
    endTs = 1596128955
  ): Array<number> {
    const values: Array<number> = [];
    let ts = startTs;

    for (let i = 0; i < m; ++i) {
      values[i] = 0.1 + 0.1 * Math.random();
    }

    for (let j = 0; j < 5; ++j) {
      const x = 1 / (0.1 + Math.random());
      const y = 2 * Math.random() - 0.5;
      const z = 10 / (0.1 + Math.random());

      for (let i = 0; i < m; i++) {
        const w = (i / m - y) * z;
        values[i] += x * Math.exp(-w * w);
      }
    }

    const result: Array<number> = [];

    for (let i = 0; i < m; ++i) {
      if (ts + step <= endTs) {
        // @ts-ignore
        result.push([ts, Math.max(0, values[i])]);
        ts += step;
      }
    }

    return result;
  }

